    private void llenarTabla() throws SQLException, ClassNotFoundException{
            Connection con= null;
            DefaultTableModel modelo = new DefaultTableModel();
            this.jTable1.setModel(modelo);
             con=conexion.getConexion();
            DriverManager.registerDriver(new com.mysql.jdbc.Driver());
            Statement s =  con.createStatement();
                try (ResultSet rs = s.executeQuery("Select * From tblProductos WHERE Estado=true")) {
                    ResultSetMetaData  rsMd = rs.getMetaData();
                    //Columnas
                    int numColumnas = rsMd.getColumnCount();
                    for(int i = 1; i<numColumnas+1; i++){
                           modelo.addColumn(rsMd.getColumnLabel(i));
                    }
                    //Filas
                    while(rs.next()){
                        Object[] fila = new Object[numColumnas];
                        for(int i = 0; i<numColumnas;i++){
                            if(7==i){
                               idM =rs.getInt(i+1);
                               fila[i]=nombreMarca(idM);

                            } else {
                                fila[i]=rs.getObject(i+1);
                            }
                         }
                        modelo.addRow(fila);
                    }
                }
        }
    
    public String nombreMarca(int id) throws SQLException, ClassNotFoundException{
         Connection con= null;
          con=conexion.getConexion();
         Statement st = con.createStatement();
           st = (Statement) con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);
           ResultSet rs = st.executeQuery("SELECT * FROM tblMarca WHERE idtblmarca = "+ id+ "");
           String nomMarca = null ;
           while(rs.next()){
                nomMarca = rs.getString("NombreMarca");
           }
          st.close();
           return nomMarca;
    }
    
    
     private void llenarMarcas() throws SQLException, ClassNotFoundException{
        Connection con= null;
         con=conexion.getConexion();
         Statement st = con.createStatement();
           st = (Statement) con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);
           ResultSet rs = st.executeQuery("SELECT idtblMarca,NombreMarca FROM tblMarca ORDER BY idtblMarca ASC");
             modeloCombo.addElement("Seleccione una Marca");
             jComboBox1.setModel(modeloCombo);
             while(rs.next()){
                 modeloCombo.addElement(new Marca(rs.getString("NombreMarca"),rs.getInt("idtblmarca")));
           }
        try {
            st.close();
        } catch (SQLException ex) {
            Logger.getLogger(IngresoProductos.class.getName()).log(Level.SEVERE, null, ex);
        }
     }
     
